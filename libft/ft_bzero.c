#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*str;
	size_t	count;

	str = (char*)s;
	count = 0;
	while (count < n)
	{
		*str = '\0';
		str++;
		count++;
	}
	return ;
}
